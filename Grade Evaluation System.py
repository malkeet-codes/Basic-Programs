
#Grade Evaluation System

grade=input("What is your grade?")
print(grade)
if grade=="A" or grade=="a":
   print("Excellent")
elif grade=="B" or grade=="b":
   print("Good")
elif grade=="C" or grade=="c":
   print("Well Done")
elif grade=="D" or grade=="d":
   print("Average")
elif grade=="E" or grade=="e":
   print("Work Hard")
else:
   print("FAIL")